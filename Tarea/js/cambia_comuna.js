jQuery(document).ready(function () {

	var iRegion = 0;
	var htmlregion = '<option value="sin-region">Seleccione Región</option>';
	var htmlcomuna = '<option value="sin-region">Seleccione Comuna</option>';

	jQuery.each(regionYcomuna.region, function () {
		htmlregion = htmlregion + '<option value="' + regionYcomuna.region[iRegion].NombreRegion + '">' + regionYcomuna.region[iRegion].NombreRegion + '</option>';
		iRegion++;
	});

	jQuery('#region').html(htmlregion);
	jQuery('#comuna').html(htmlcomuna);

	jQuery('#region').change(function () {
		var iregion = 0;
		var valorRegion = jQuery(this).val();
		var htmlComuna = '<option value="sin-comuna">Seleccione comuna</option>';
		jQuery.each(regionYcomuna.region, function () {
			if (regionYcomuna.region[iregion].NombreRegion == valorRegion) {
				var icomuna = 0;
				jQuery.each(regionYcomuna.region[iregion].comuna, function () {
					htmlComuna = htmlComuna + '<option value="' + regionYcomuna.region[iregion].comuna[icomuna] + '">' + regionYcomuna.region[iregion].comuna[icomuna] + '</option>';
					icomuna++;
				});
			}
			iregion++;
		});
		jQuery('#comuna').html(htmlComuna);
	});
	jQuery('#comuna').change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('Seleccione Región');
		} else if (jQuery(this).val() == 'sin-comuna') {
			alert('Seleccione Comuna');
		}
	});
	jQuery('#region').change(function () {
		if (jQuery(this).val() == 'sin-region') {
			alert('Seleccione Región');
		}
	});

});
